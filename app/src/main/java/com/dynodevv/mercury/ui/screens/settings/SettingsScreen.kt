@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun SettingsScreen(
    onNavigateBack: () -> Unit,
    onNavigateToAiSettings: () -> Unit,
    onNavigateToSearchSettings: () -> Unit,
    onNavigateToPrivacySettings: () -> Unit,
    viewModel: SettingsViewModel = hiltViewModel()
) {
    Scaffold(
        topBar = {
            TopAppBar(
                title = { Text(stringResource(R.string.settings)) },
                navigationIcon = {
                    IconButton(onClick = onNavigateBack) {
                        Icon(Icons.AutoMirrored.Filled.ArrowBack, contentDescription = stringResource(R.string.back))
                    }
                },
                colors = TopAppBarDefaults.topAppBarColors(
                    containerColor = MaterialTheme.colorScheme.surfaceContainerHigh
                )
            )
        }
    ) { paddingValues ->
        LazyColumn(
            modifier = Modifier
                .fillMaxSize()
                .padding(paddingValues),
            contentPadding = PaddingValues(16.dp),
            verticalArrangement = Arrangement.spacedBy(12.dp)
        ) {
            item {
                SettingsCategory(
                    title = stringResource(R.string.appearance),
                    icon = Icons.Filled.ColorLens,
                    onClick = onNavigateBack // Appearance is minimal, can expand later
                )
            }
            item {
                SettingsCategory(
                    title = stringResource(R.string.search_engine),
                    icon = Icons.Filled.Search,
                    onClick = onNavigateToSearchSettings
                )
            }
            item {
                SettingsCategory(
                    title = stringResource(R.string.ai),
                    icon = Icons.Filled.SmartToy,
                    onClick = onNavigateToAiSettings
                )
            }
            item {
                SettingsCategory(
                    title = stringResource(R.string.privacy),
                    icon = Icons.Filled.Policy,
                    onClick = onNavigateToPrivacySettings
                )
            }
        }
    }
}

@Composable
private fun SettingsCategory(
    title: String,
    icon: ImageVector,
    onClick: () -> Unit
) {
    Card(
        modifier = Modifier
            .fillMaxWidth()
            .clickable(onClick = onClick),
        colors = CardDefaults.cardColors(
            containerColor = MaterialTheme.colorScheme.surfaceContainerHigh
        )
    ) {
        Row(
            modifier = Modifier
                .fillMaxWidth()
                .padding(16.dp),
            verticalAlignment = Alignment.CenterVertically
        ) {
            Icon(
                imageVector = icon,
                contentDescription = null,
                tint = MaterialTheme.colorScheme.primary
            )
            Spacer(modifier = Modifier.width(16.dp))
            Text(
                text = title,
                style = MaterialTheme.typography.bodyLarge,
                modifier = Modifier.weight(1f)
            )
            Icon(
                imageVector = Icons.AutoMirrored.Filled.KeyboardArrowRight,
                contentDescription = null,
                tint = MaterialTheme.colorScheme.onSurfaceVariant
            )
        }
    }
}
